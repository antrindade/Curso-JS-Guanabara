//While
let c = 1

while(c <= 5){
    document.write(`Passo ${c} <br>`)
    c++
}

// Do while
do {
    document.write(`Passo ${c} <br>`)
    c++
} while (c <= 9)

//FOR
document.write("Vamos começar... vamos na contagem do foguete <br>")
for(let c=1; c<=10; c++){
    document.write(`${c} <br>`)
}
document.write("Foguete lançado")